#include "RNOH/PackageProvider.h"
#include "generated/RNOHGeneratedPackage.h"
#include "NoCodegenArkTSSamplePackage.h"
#include "NoCodegenCAPISamplePackage.h"
#include "CodegenSamplePackage.h"

using namespace rnoh;

std::vector<std::shared_ptr<Package>> PackageProvider::getPackages(
    Package::Context ctx) {
  return {
      std::make_shared<RNOHGeneratedPackage>(ctx), // generated by codegen v1
      std::make_shared<CodegenSamplePackage>(ctx),
      std::make_shared<NoCodegenArkTSSamplePackage>(ctx),
      std::make_shared<NoCodegenCAPISamplePackage>(ctx),
  };
}